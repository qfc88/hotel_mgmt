    private void DisplayPay(){
        try{
            ResultSet rs = Operation.getData("SELECT Customer.CName, "
                    + "RoomType.Name, RoomType.Capacity, "
                    + "Payment.* "
                    + "FROM Booking, Customer, Room, RoomType, Payment "
                    + "WHERE Booking.CustomerID = Customer.CustomerID "
                    + "AND Booking.RoomNumber = Room.RoomNumber "
                    + "AND Room.TypeID = RoomType.TypeID "
                    + "AND Booking.Status = 'Confirm'");
            PayTable.setModel(DbUtils.resultSetToTableModel(rs));
            ResultSet revenue = Operation.getData("Select SUM(Amount) from Payment");
            while (revenue.next()){
            Revenue.setText(String.valueOf(revenue.getInt("SUM(Amount)"))+" $");
            }
        }
        catch (Exception e){
         JOptionPane.showMessageDialog(null, e);
        }
    }

