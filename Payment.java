    private void PayTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PayTableMouseClicked
        // TODO add your handling code here:
        int row = PayTable.getSelectedRow();
        setRow(row);
        DefaultTableModel model = (DefaultTableModel)PayTable.getModel();
        ResultSet rs = Operation.getData("select RoomType.Price, RoomType.Capacity, RoomType.Description, Booking.NoOfDays from RoomType, Room, Booking, Payment"
                + " where Booking.BookingID = "+model.getValueAt(row, 4)+" and Room.RoomNumber = Booking.RoomNumber "
                        + "and Room.TypeID = RoomType.TypeID");
        CNameDisplay.setText(model.getValueAt(row, 0).toString());
        RTDisplay.setText(model.getValueAt(row, 1).toString());
        CapacityDisplay.setText(model.getValueAt(row, 2).toString());
        try {
            rs.next();
            double tempPrice = rs.getBigDecimal("Price").doubleValue();
            double tempCapacity = rs.getInt("Capacity");
            double tempDays = rs.getInt("NoOfDays");
            AmountBox.setText(String.valueOf((tempPrice+tempCapacity*50)*tempDays));
            DescriptionBox.setText(rs.getString("Description"));
        } catch (SQLException ex) {
            Logger.getLogger(Payment.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_PayTableMouseClicked

